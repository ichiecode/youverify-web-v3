        <h3>1.The Client</h3>

        <p>
          Clients (“business customers”) are organisations that have contracted
          and asked Youverify to verify an identity or verify an address or
          carry out verifications related to that identity. Once we have
          verified an identity or completed the requested verification or check,
          we share the results (“report”) with the client in a Youverify Report,
          as described further below. The client then decides how they want to
          proceed with the user based on the results. In some cases, the client
          might ask for additional information before making a decision. Also,
          some clients only ask us to carry out a check if a previous check was
          marked as verified or not verified. This ensures we only do the
          minimum number of verifications needed.
        </p>

        <h3>2. The User</h3>

        <p>
          Users are individuals whose identities or personal information we
          verify or otherwise validate on behalf of our clients. We collect
          users’ information from our clients or directly from the users
          themselves. This information might include the name of the user, the
          unique identification number and image of the user’s identity document
          (e.g. passport, driver’s license, biometric verification number,
          permanent voter’s card, national ID), photos (at times, taken in quick
          succession for anti-fraud purposes) or a video of the user, the
          biometric facial identifiers in those images, and the user’s date of
          birth (this is collected to ensure that the client or the user have
          enough information about the subject before requesting Youverify to
          verify an identity). This enables us to help the client verify that
          the user is the true owner of the identity document and has not shown
          signs of identity fraud. In some cases, the information requested
          might include the user’s physical address, the user’s guarantors’
          identity information, the user’s employment information, and the
          user’s education record. This enables us to help the client verify
          that the user information as part of our client compliance due
          diligence and confirm that the information is true. In some
          circumstances, we may also collect device identifiers and IP addresses
          to help us understand whether a device has previously been used in
          relation to suspected fraudulent activity and whether Youverify is
          permitted to provide Verification Services in the country in which the
          user is located. To further combat fraud, we also collect identity
          information that has been leaked or otherwise made available on the
          internet.
        </p>

        <h3>3. The Data Providers</h3>

        <p>
          Data providers are used to provide additional information to carry out
          specific checks. For example, if we need to verify a national
          identification number (“NIN”), we’ll match the information provided by
          the user with the National Identity Management Commission (“NIMC”).
          Another example, if we need to verify the user’s right to drive, we
          might ask the appropriate government driving body to verify that
          user’s information. When our client asks us to verify an address, we
          ask our field agents to verify the information, in this case they are
          our data provider for the verification request.
        </p>

        <p>
          We also keep logs of how our clients, users, and data providers
          interact with our verification services. This might include timestamps
          and location information of when the information was submitted to
          Youverify, and information about the device used to submit that
          information. Sometimes, we receive information we don’t need to
          provide our Verification Services. For example, instead of a picture
          of a person, a user might upload a completely unrelated image. When
          this happens, we seek to delete this data.
        </p>

        <p>
          We use information to deliver and maintain our verification services
