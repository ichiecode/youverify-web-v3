              name, nicknames, birth details, pet’s name, etc. as your
              passwords, instead use a concoction of lower case and upper case
              letters, numbers and special symbols, and also ensure that your
              passwords contain more than 8 characters. Furthermore, use
              different passwords for your various online accounts and try to
              change them frequently. Lastly, store all your passwords in a
              secure place.
            </p>

